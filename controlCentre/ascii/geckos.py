def random_gecko() -> str:
    return random.choice(GECKOS)

def blink_gecko(art: str) -> str:
    blink = art
    replacements = [
        ("oo", "--"),
        ("OO", "--"),
        ("o  o", "-  -"),
        ("@@@       @@@", "---       ---"),
        ("@@@@@     @@@@@", "-----     -----"),
        ("@@@@@", "-----"),
        ("()    ", "--    "),
        ("    ()", "    --"),
    ]
    for old, new in replacements:
        blink = blink.replace(old, new, 2)
    return blink
